const test        = require('mocha').it
const assert      = require('assert')
const EightQueens = require('.')

test('eight queens', () => {
    assert(EightQueens(['(2,1)', '(4,2)', '(6,3)', '(8,4)', '(3,5)', '(1,6)', '(7,7)', '(5,8)']) === 'true')
    assert(EightQueens(['(2,1)', '(4,2)', '(6,3)', '(8,4)', '(5,5)', '(3,6)', '(1,7)', '(7,8)']) === 'true')
    assert(EightQueens(['(2,1)', '(4,2)', '(4,4)', '(8,4)', '(3,4)', '(1,6)', '(7,7)', '(5,8)']) === '(4,2)') // vert
    assert(EightQueens(['(2,1)', '(4,2)', '(6,3)', '(8,3)', '(3,4)', '(1,6)', '(7,7)', '(5,8)']) === '(6,3)') // hori
    assert(EightQueens(['(2,1)', '(4,3)', '(6,3)', '(8,4)', '(3,4)', '(1,6)', '(7,7)', '(5,8)']) === '(2,1)') // 45diag
    assert(EightQueens(['(2,1)', '(4,2)', '(3,3)', '(8,4)', '(7,5)', '(3,6)', '(1,7)', '(7,8)']) === '(4,2)') // 135diag
    assert(EightQueens(['(2,1)', '(5,3)', '(6,3)', '(8,4)', '(3,4)', '(1,8)', '(7,7)', '(5,8)']) === '(5,3)') // vert
})
